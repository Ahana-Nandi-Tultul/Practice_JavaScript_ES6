let result = 10000

if (result > 79 && result <= 100)
{
    console.log("A+")
}
else if (result > 69 && result <= 79)
{
    console.log("A")
}
else if (result > 59  && result <= 69)
{
    console.log("B")
}
else if (result > 49  && result <= 59)
{
    console.log("C")
}
else if (result > 39  && result <= 49)
{
    console.log("D")
}
else if (result <= 39)
{
    console.log("F")
}
else
{
    console.log("Invalid Input")
}